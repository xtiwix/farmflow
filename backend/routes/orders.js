// Create order with task generation
router.post('/', auth, async (req, res) => {
  try {
    const { customerId, dateType, harvestDate, startDate, deliveryDate, deliveryOffset, 
            isRecurring, recurringFrequency, recurringEndDate, notes, items } = req.body;
    
    // Generate order number
    const count = await Order.count();
    const orderNumber = `ORD-${String(count + 1).padStart(5, '0')}`;
    
    // Calculate dates
    let finalHarvestDate = harvestDate;
    let finalDeliveryDate = deliveryDate;
    let finalStartDate = startDate;
    
    if (dateType === 'harvest' && harvestDate) {
      finalDeliveryDate = subtractDays(harvestDate, deliveryOffset || 0);
    } else if (dateType === 'start' && startDate && items?.length > 0) {
      // Calculate harvest date from start date (use longest crop cycle)
      let maxDays = 0;
      for (const item of items) {
        const product = await Product.findByPk(item.productId, { include: [Crop, MushroomSpecies] });
        if (product?.Crop) maxDays = Math.max(maxDays, product.Crop.daysToHarvest || 7);
        if (product?.MushroomSpecies) maxDays = Math.max(maxDays, product.MushroomSpecies.daysToFirstHarvest || 7);
      }
      finalHarvestDate = addDays(startDate, maxDays);
      finalDeliveryDate = subtractDays(finalHarvestDate, deliveryOffset || 0);
    }
    
    // Get customer for pricing
    const customer = await Customer.findByPk(customerId, { include: [PriceTier] });
    
    // Calculate totals
    let subtotal = 0;
    const orderItems = [];
    
    for (const item of items || []) {
      const product = await Product.findByPk(item.productId);
      if (!product) continue;
      
      // Get price for customer's tier
      let unitPrice = 0;
      if (customer?.priceTierId) {
        const priceRecord = await ProductPrice.findOne({
          where: { productId: product.id, tierId: customer.priceTierId }
        });
        unitPrice = priceRecord ? parseFloat(priceRecord.price) : 0;
      }
      
      const itemTotal = unitPrice * item.quantity;
      subtotal += itemTotal;
      
      orderItems.push({
        productId: product.id,
        quantity: item.quantity,
        unitPrice,
        total: itemTotal,
        cropId: product.cropId,
        speciesId: product.speciesId
      });
    }
    
    // Create order
    const order = await Order.create({
      farmId: req.user.farmId,
      customerId,
      orderNumber,
      dateType: dateType || 'harvest',
      harvestDate: finalHarvestDate,
      startDate: finalStartDate,
      deliveryDate: finalDeliveryDate || finalHarvestDate,
      deliveryOffset: deliveryOffset || 0,
      isRecurring: isRecurring || false,
      recurringFrequency,
      recurringEndDate,
      subtotal,
      total: subtotal,
      notes
    });
    
    // Create order items
    for (const item of orderItems) {
      await OrderItem.create({ ...item, orderId: order.id });
    }
    
    // Generate tasks
    const taskCount = await generateOrderTasks(order, req.user.farmId);
    
    res.status(201).json({ 
      order, 
      message: `Order created with ${taskCount} tasks generated` 
    });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

// Update order
router.put('/:id', auth, async (req, res) => {
  try {
    const order = await Order.findByPk(req.params.id);
    if (!order) return res.status(404).json({ error: 'Order not found' });
    
    const { customerId, dateType, harvestDate, startDate, deliveryDate, deliveryOffset, 
            isRecurring, recurringFrequency, recurringEndDate, notes, items } = req.body;
    
    // Update order fields
    await order.update({
      customerId,
      dateType,
      harvestDate,
      startDate,
      deliveryDate,
      deliveryOffset,
      isRecurring,
      recurringFrequency,
      recurringEndDate,
      notes
    });
    
    // Update items if provided
    if (items) {
      // Delete existing items
      await OrderItem.destroy({ where: { orderId: order.id } });
      
      // Get customer for pricing
      const customer = await Customer.findByPk(customerId, { include: [PriceTier] });
      let subtotal = 0;
      
      // Create new items
      for (const item of items) {
        const product = await Product.findByPk(item.productId);
        if (!product) continue;
        
        let unitPrice = 0;
        if (customer?.priceTierId) {
          const priceRecord = await ProductPrice.findOne({
            where: { productId: product.id, tierId: customer.priceTierId }
          });
          unitPrice = priceRecord ? parseFloat(priceRecord.price) : 0;
        }
        
        const itemTotal = unitPrice * item.quantity;
        subtotal += itemTotal;
        
        await OrderItem.create({
          orderId: order.id,
          productId: product.id,
          quantity: item.quantity,
          unitPrice,
          total: itemTotal,
          cropId: product.cropId,
          speciesId: product.speciesId
        });
      }
      
      await order.update({ subtotal, total: subtotal });
    }
    
    // Regenerate tasks
    const taskCount = await generateOrderTasks(order, req.user.farmId);
    
    res.json({ order, message: `Order updated with ${taskCount} tasks regenerated` });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

