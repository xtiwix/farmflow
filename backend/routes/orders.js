// Delete single order (with task cleanup)
router.delete('/:id', auth, async (req, res) => {
  try {
    const order = await Order.findByPk(req.params.id);
    if (!order) return res.status(404).json({ error: 'Order not found' });
    
    // Delete associated tasks
    await Task.destroy({
      where: { sourceType: 'order', sourceId: order.id }
    });
    
    // Delete order (cascade deletes items)
    await order.destroy();
    
    res.json({ message: 'Order and associated tasks deleted' });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

// Bulk delete orders
router.post('/bulk-delete', auth, async (req, res) => {
  try {
    const { orderIds } = req.body;
    if (!orderIds || !Array.isArray(orderIds)) {
      return res.status(400).json({ error: 'orderIds array required' });
    }
    
    // Delete associated tasks
    await Task.destroy({
      where: { sourceType: 'order', sourceId: { [Op.in]: orderIds } }
    });
    
    // Delete orders
    const deleted = await Order.destroy({
      where: { id: { [Op.in]: orderIds } }
    });
    
    res.json({ message: `${deleted} orders and associated tasks deleted` });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

module.exports = router;
